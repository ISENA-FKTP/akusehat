export const DataSakit = {
  getDataRekamMedis() {
    const data_rekam_medis = data_sakit_polisi
      .filter((data) => data.data_rekam_medis.length > 0)
      .flatMap((data) =>
        data.data_rekam_medis.map((element) => ({
          nrp: data.nrp,
          nama: data.nama,
          pangkat: data.pangkat,
          satuan_kerja: data.satuan_kerja,
          tanggal: element.tanggal,
          keterangan: element.keterangan,
          pdf_url: element.pdf_url,
        }))
      );

    return Promise.resolve(data_rekam_medis);
  },

  getDataHomeVisit() {
    const data_home_visit = data_sakit_polisi
      .filter((data) => data.data_home_visit.length > 0)
      .flatMap((data) =>
        data.data_home_visit.map((element) => ({
          nrp: data.nrp,
          nama: data.nama,
          pangkat: data.pangkat,
          satuan_kerja: data.satuan_kerja,
          tanggal: element.tanggal,
          keluhan: element.keluhan,
          pemeriksaan_fisik: element.pemeriksaan_fisik,
          diagnosis: element.diagnosis,
          terapi: element.terapi,
          saran_medis: element.saran_medis,
        }))
      );

    return Promise.resolve(data_home_visit);
  },

  getDataSakit() {
    // Filter the data_sakit_polisi array to include only those with non-empty data_sakit arrays
    const data_sakit = data_sakit_polisi
      .filter((data) => data.data_sakit.length > 0)
      .flatMap((data) =>
        data.data_sakit.map((element) => ({
          nrp: data.nrp,
          nama: data.nama,
          pangkat: data.pangkat,
          satuan_kerja: data.satuan_kerja,
          tanggal: element.tanggal,
          jenis_sakit: element.jenis_sakit,
          jenis_perawatan: element.jenis_perawatan,
          sumber_biaya: element.sumber_biaya,
          awal_sakit: element.awal_sakit,
          lama_cuti: element.lama_cuti,
          wfh: element.WFH,
          keterangan: element.Keterangan,
        }))
      );

    return Promise.resolve(data_sakit);
  },

  setDataSakit(
    nrp,
    tanggal,
    jenis_sakit,
    jenis_perawatan,
    sumber_biaya,
    awal_sakit,
    lama_cuti,
    WFH,
    Keterangan
  ) {
    const data_sakit = data_sakit_polisi.find((data) => data.nrp === nrp);

    data_sakit.data_sakit.push({
      tanggal: tanggal,
      jenis_sakit: jenis_sakit,
      jenis_perawatan: jenis_perawatan,
      sumber_biaya: sumber_biaya,
      awal_sakit: awal_sakit,
      lama_cuti: lama_cuti,
      WFH: WFH,
      Keterangan: Keterangan,
    });
  },

  setDataHomeVisit(
    nrp,
    tanggal,
    keluhan,
    pemeriksaan_fisik,
    diagnosis,
    terapi,
    saran_medis
  ) {
    const data_sakit = data_sakit_polisi.find((data) => data.nrp === nrp);

    data_sakit.data_home_visit.push({
      tanggal: tanggal,
      keluhan: keluhan,
      pemeriksaan_fisik: pemeriksaan_fisik,
      diagnosis: diagnosis,
      terapi: terapi,
      saran_medis: saran_medis,
    });
  },

  setDataRekamMedis(nrp, tanggal, keterangan, pdf_url) {
    const data_sakit = data_sakit_polisi.find((data) => data.nrp === nrp);

    data_sakit.data_rekam_medis.push({
      tanggal: tanggal,
      keterangan: keterangan,
      pdf_url: pdf_url,
    });
  },

  getData(nrp) {
    const data_sakit = data_sakit_polisi.find((data) => data.nrp === nrp);
    return Promise.resolve(data_sakit);
  },
};

